	{
		//TaskInitAbility("Poisonous");
	}
	public override void RegisterEventEffect()
	{
		owner.myPlayer.AddCallBack(new CallBackBeforeAttack(this,this.CauseEvent));
	}
	public void CauseEvent(Card sc,Card tc,ZoneType sz,ZoneType tz)
	{
		if(CondInPlay())
		{
			if(sc==owner)
			{
				List<Card> adj = FindAdj(tc);
				List<Card> ToListDamage = new List<Card>();
				
				Card ToDmg = adj.Choice(owner.myPlayer.randomGen);
				if(ToDmg!=null)
				{
					ToListDamage.Add(ToDmg);
				}
				ToListDamage.Add(tc);
				TaskDamageTargets(ToListDamage,owner.GetTag(GameTag.Attack));
			}
		}
	}
}
