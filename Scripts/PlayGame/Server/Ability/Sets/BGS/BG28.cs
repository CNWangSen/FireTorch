//NAME=冒进的锚手
//TEXT=<b>战斗开始时：</b> 和最近的敌方随从战斗至死！
//MAJ=NEUTRAL|COST=4|ATK=3|HP=13|RACE=PIRATE
	private float GetX(int myindex,int myN)
	{
        float minx = -11.86f;
        float maxx = 11.21f;
        float cx = (minx + maxx) / 2f;
        float bw = (maxx - minx);
        float c1 = -bw / 12f;
        float c2 = bw / 6f;
        float myx= cx + c1 * (myN - 1) + c2 * myindex;
        return myx;
	}
	public override void OnCombatStart()
	{
		float myX = GetX(owner.myPlayer.board.IndexOf(owner),owner.myPlayer.board.Count);
		Card Near1 = null;
		float Dis1 = 1000f;

		foreach(Card c in owner.myPlayer.otherPlayer.board)
		{
			if(c.GetTag(GameTag.Health)<=0)
			{
				continue;
			}
			float X = GetX(owner.myPlayer.otherPlayer.board.IndexOf(c),owner.myPlayer.otherPlayer.board.Count);
			float Dis = X-myX;
			if(Dis<0)
			{
				Dis=-Dis;
			}
			if(Near1==null)
			{
				Near1=c;
				Dis1=Dis;
			}
			else{
				if(Dis<Dis1)
				{
					Near1=c;
					Dis1=Dis;
				}
			}
		}

		if(Near1!=null)
		{
			new EffectChange(owner.myPlayer,owner,Near1).Resolve();
			int Cnt=0;
			while(Cnt<100 && Near1.GetTag(GameTag.Health)>0 && owner.GetTag(GameTag.Health)>0)
			{
				new EffectAttack(owner.myPlayer,owner,Near1).Resolve();
				new CreateDelayCommand(0.3f).AddToQueue();
				Cnt+=1;
			}
		}
	}
}
