    public override void Init()
    {
        TaskInitAbility("Taunt");
    }
    public override void Deathrattle()
    {
        List<Card> Pool = new List<Card>();
        foreach(Card c in owner.myPlayer.otherPlayer.deck)
        {
            if(c.cardType==CardType.Minion)
            {
                Pool.Add(c);
            }
        }
        Card ToMove = Pool.Choice(owner.myPlayer.randomGen);
        if(ToMove!=null)
        {
            new EffectMove(owner.myPlayer.otherPlayer,ToMove,ZoneType.Deck,ZoneType.Board).Resolve();
        }
    }
}

