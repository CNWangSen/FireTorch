		base.RegisterEventEffect();
	}
	public override void UnRegisterEventEffect()
	{
		
	}
	public override void Battlecry(Card target=null)
	{
		base.Battlecry(target);
		List<Card> targets=new List<Card>();
		List<int> Buffs=new List<int>();
		foreach(Card c in owner.myPlayer.board)
		{
			if(c.cardDbf.DbfId==owner.cardDbf.DbfId && c!=owner)
			{
				targets.Add(c);
				Buffs.Add(1);
			}
		}
		new EffectChange(owner.myPlayer,owner,targets:targets,atkChanges:Buffs,hpChanges:Buffs).Resolve();
		owner.myPlayer.AddCallBack(new CallBackAfterMove(this,TTN_401CauseEvent));
		owner.myPlayer.AddCallBack(new CallBackAfterCreate(this,TTN_401CauseEvent));
	}
	public void TTN_401CauseEvent(Card sc,Card tc,ZoneType sz,ZoneType tz)
	{
		if(tz==ZoneType.Board && (sc.cardDbf.DbfId==owner.cardDbf.DbfId))
		{
			new EffectChange(owner.myPlayer,sc,sc,0,1,1).Resolve();
		}
	}
