	public override void Battlecry(Card target=null)
	{
		Card MaxCost = null;
		foreach(Card c in owner.myPlayer.hand)
		{
			if(MaxCost==null)
			{
				MaxCost=c;
			}
			else{
				if(c.GetTag(GameTag.Mana)>MaxCost.GetTag(GameTag.Mana))
				{
					MaxCost=c;
				}
			}
		}
		if(MaxCost!=null)
		{
			int dmg=MaxCost.GetTag(GameTag.Mana);
			new EffectMove(owner.myPlayer,MaxCost,ZoneType.Hand,ZoneType.Grave).Resolve();
			TaskDamageTargets(owner.myPlayer.board.Merge(owner.myPlayer.otherPlayer.board),dmg);
		}
	}
}
