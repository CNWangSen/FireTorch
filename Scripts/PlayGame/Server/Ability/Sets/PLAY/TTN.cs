	{
		Card Left1=null;
		Card Left2=null;
		foreach(Card c in owner.myPlayer.hand)
		{
			if(c!=owner)
			{
				if(Left1!=null)
				{
					Left2=c;
				}
				else{
					Left1=c;
				}
			}
		}
		if(Left1!=null)
		{
			new EffectMove(owner.myPlayer,Left1,ZoneType.Hand,ZoneType.Deck).Resolve();
		}
		if(Left2!=null)
		{
			new EffectMove(owner.myPlayer,Left2,ZoneType.Hand,ZoneType.Deck).Resolve();
		}
		TaskDraw();
		TaskDraw();
		TaskDraw();
	}
}
